// Regular function
function add(a, b) {
    return a + b;
  }
  
  // Arrow function
  //Arrow functions provide a more concise syntax for writing functions.
  const addArrow = (a, b) => a + b;
  
  console.log(add(2, 3));        // Output: 5
  console.log(addArrow(2, 3));   // Output: 5
  